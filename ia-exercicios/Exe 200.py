# 3. Crie uma função multiplica que receba dois números e retorne o produto.
# Dentro da função, declare uma variável local chamada resultado e use-a para armazenar a multiplicação.

def multiplicacao(a,b):
    resultado = a * b
    return resultado

#programa principal
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
r = multiplicacao(num1,num2)
print(r)



