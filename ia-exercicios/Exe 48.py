#Em maiúsculas
#Em minúsculas
#Somente a primeira letra maiúscula
palavra = str(input("Digite uma palavra: "))
print(f"A palavra original é: {palavra}")
print(f"A palavra em maiúsculo é: {palavra.upper()}")
print(f"A palavra em minusculo é: {palavra.lower()}")
print(f"A palavra com a primeira letra maiúscula é: {palavra.capitalize()}")
